// abs
var number = -5;
console.log(Math.abs(number));

// round
var number1 = 58658.5555;
console.log(Math.round(number1));

// ceil
var number2 = 5.468;
console.log(Math.ceil(number2));

// floor
var number3 = 5.999;
console.log(Math.floor(number3));

// random
var number4 = Math.random();
console.log(number4);

// get random round value between 0 to 100
var number5 = Math.round(Math.random()*100);
console.log(number5);


// another way to get random round value between 0 to 100
var number5 = Math.random()*100;
number5 = Math.round(number5);
console.log(number5)

